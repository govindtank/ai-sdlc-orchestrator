"""
Setup Script for AI SDLC Orchestrator Development Environment

This script initializes the development environment, sets up databases, and runs initial tests.
"""
import os
import sys
from pathlib import Path


def setup_environment():
    """Configure environment variables for local development."""
    
    # Create .env if it doesn't exist (for demo purposes)
    env_path = Path(__file__).parent / ".env"
    
    if not env_path.exists():
        print("⚠️  No .env file found. Creating with default settings...")
        
        example_env = Path(__file__).parent / "backend" / ".env.example"
        if example_env.exists():
            content = f"# Development Environment (Generated by setup script)\n"
            
            # Read and modify example env for development
            lines = {}
            with open(example_env) as f:
                for line in f:
                    line = line.strip()
                    if line.startswith('#') or not line:
                        continue
                    
                    # Extract key=value
                    if '=' in line:
                        key, value = line.split('=', 1)
                        key = key.strip()
                        value = value.strip().strip('"\'')
                        
                        # Replace placeholders for development
                        if '***' in value:
                            value = f'{key}_DEVELOPMENT_VALUE_2026'
                        elif value.startswith('your-'):
                            value = 'temp_key_for_development_demo'
                        
                        content += f"{key}={value}\n"
            
            with open(env_path, 'w') as f:
                f.write(content)
            
            print(f"✅ Created {env_path}")
        else:
            # Create basic env if no example available
            with open(env_path, 'w') as f:
                f.write("# Development Setup\n")
                f.write("LLM_PROVIDER=mock\n")
                f.write("APP_ENV=development\n")
                f.write("DEBUG=True\n")
    
    return True


