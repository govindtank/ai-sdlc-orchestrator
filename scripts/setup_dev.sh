def check_dependencies():
    """Verify required Python packages are installed."""
    print("\n📦 Checking dependencies...")
    
    required_packages = [
        'fastapi',
        'uvicorn',
        'psycopg2-binary',
        'python-jose',
        'passlib',
        'openai' if os.getenv('LLM_PROVIDER') != 'mock' else '',
        'requests' if os.getenv('HF_ENDPOINT_URL') else ''
    ]
    
    missing = []
    for package in required_packages:
        try:
            __import__(package.replace('-', '_'))
            print(f"   ✅ {package}")
        except ImportError:
            missing.append(package)
    
    if missing:
        print(f"\n⚠️  Missing packages: {', '.join(missing)}")
        print("Install with:")
        print("   pip install -r backend/requirements.txt")
        return False
    
    return True


def run_tests():
    """Run test suite."""
    print("\n🧪 Running tests...")
    
    import subprocess
    result = subprocess.run(
        ['pytest', '-v', 'tests/', '--tb=short'],
        capture_output=True,
        text=True,
        cwd=str(Path(__file__).parent)
    )
    
    print(result.stdout if result.stdout else result.stderr)
    
    return result.returncode == 0


def show_health_check():
    """Run health check against API."""
    import requests
    
    url = "http://localhost:8000/health" if os.getenv("APP_ENV") == "development" else \
          "http://localhost:3000/api/v1/health"  # or your actual backend URL
    
    print(f"\n🩺 API Health Check...")
    try:
        response = requests.get(url, timeout=5)
        if response.status_code == 200:
            data = response.json()
            print(f"✅ API is healthy: {data}")
        else:
            print(f"⚠️  API returned status {response.status_code}")
    except Exception as e:
        print(f"ℹ️  API not running at {url} (this is expected if services aren't started)")


